public class Сycle {

    // Необходимо с помощью цикла вывести на экран числа от 1 до 50.

    static void cycleFor(){

        for (int i = 1; i <= 50; i++){
            System.out.println(i);
        }
    }

    static void cycleWhile(){

        int y = 1;

        while (y <=50){

            System.out.println(y);
            y++;
        }
    }

}
